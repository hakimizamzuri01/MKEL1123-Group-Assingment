int main(void)
{

  HAL_Init();

  SystemClock_Config();

  // Enable GPIOC clock
  RCC_APB2ENR |= RCC_IOPCEN;
  // Configure PC13 as push-pull output
  GPIOC_CRH &= ~(0xF << 20); // Clear MODE13 and CNF13
  GPIOC_CRH |= (PC13_OUTPUT | PC13_PUSH_PULL);  // Set as output push-pull

  //MX_GPIO_Init();

  while (1)
  {
	    GPIOC_ODR ^= (1 << 13);  // Toggle PC13 (bare-metal style)
	    HAL_Delay(500);          // Use HAL delay (500 ms)

  }
}

